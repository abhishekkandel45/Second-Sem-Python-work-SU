#Demostration of built-in function pow() and math.pow()
import math
# returns the 2 to the power of 3 in a float
print (math.pow(2,3))
# returns the 2 to the power of 3 in an integer
print (pow(2,3))
#Returns 2 to the power of 3 in a integer and then do modulo of 3 to get the remainder
print (pow(2,3,3))